    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        // TODO add your handling code here:
        try{

            int codigo = Integer.parseInt(jtCodigo.getText());
            
            //me fijo si existe un producto con ese codigo?
            for(Producto prod:MenuView.listaProductos){
                
                if(prod.getCodigo() == codigo){
                    
                    // Si existe lo muestro por pantalla
                    jtDescripcion.setText(prod.getDescripcion());
                    jtPrecio.setText(prod.getPrecio() + "");
                    jcRubro.setSelectedItem(prod.getRubro());
                    jtStock.setText(prod.getStock() + "");
                    
                    // Y confirmo que sea el que se desea eliminar
                    if(JOptionPane.showConfirmDialog(this, "Esta seguro que desea eliminar el siguiente producto") == 0){
                        
                        // Lo elimino
                        MenuView.listaProductos.remove(prod);
                        JOptionPane.showMessageDialog(this, "Producto con codigo: " + codigo + " eliminado");
                        
                        // Luego de eliminar, limpio la pantalla
                        jbNuevoActionPerformed(evt);
                        return;
                    }
                    
                    // Si le da a "no" o a "cancel", cancelo y limpio la pantalla, y salgo del bucle
                    JOptionPane.showMessageDialog(this, "Accion cancelada");
                    jbNuevoActionPerformed(evt);
                    return;
                }                               
            }
            
            JOptionPane.showMessageDialog(this, "No existe un producto con ese codigo");           
            
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El codigo debe ser un entero");
        }
    }//GEN-LAST:event_jbEliminarActionPerformed

    private void cargarCombo() {
        jcRubro.addItem(Categoria.LIMPIEZA);
        jcRubro.addItem(Categoria.COMESTIBLE);
        jcRubro.addItem(Categoria.PERFUMERIA);
        jcRubro.setSelectedIndex(-1);
    }

