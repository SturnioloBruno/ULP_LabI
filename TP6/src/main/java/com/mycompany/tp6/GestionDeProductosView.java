    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        try{
            int codigo = Integer.parseInt(jtCodigo.getText());
        
            for(Producto prod: MenuView.listaProductos) {
            
                if( prod.getCodigo() == codigo) {
                    Producto prodEncontrado = prod;
                    jtDescripcion.setText(prodEncontrado.getDescripcion());
                    jtPrecio.setText(prodEncontrado.getPrecio() + "");
                    jcRubro.setSelectedItem(prodEncontrado.getRubro());
                    jtStock.setText(prodEncontrado.getStock() + "");
                    return;
                }
            }
        
            // si recorrió toda la lista y ninguna coincidía, no existe un prod con ese codigo.
            JOptionPane.showMessageDialog(this, "No existe un producto con ese id");
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "El codigo debe ser un entero");
        }
        
    }//GEN-LAST:event_jbBuscarActionPerformed

